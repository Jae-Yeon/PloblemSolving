//백준 온라인 저지 1309번 동물원
#include<stdio.h>
int n, a[100005][3];
int main() {
	scanf("%d", &n);
	a[1][0] = a[1][1] = a[1][2] = 1;
	for (int i = 2; i <= n + 1; i++) {
		a[i][0] = (a[i - 1][0] + a[i - 1][1] + a[i - 1][2]) % 9901;
		a[i][1] = (a[i - 1][0] + a[i - 1][2]) % 9901;
		a[i][2] = (a[i - 1][0] + a[i - 1][1]) % 9901;
	}
	printf("%d", a[n+1][0]);
}
